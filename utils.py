def kj2kcal(kj):
    """
    千焦转千卡
    :param kj:
    :return:
    """
    return kj * 0.2389


def kcal2kj(kcal):
    """
    千卡转千焦
    :param kcal:
    :return:
    """
    return kcal / 0.2389
